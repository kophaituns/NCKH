#!/usr/bin/env node

// Test script for database-stored questions verification
const axios = require('axios');
const mysql = require('mysql2/promise');
require('dotenv').config();

async function testGeneratedQuestionsInDatabase() {
  let connection;
  
  try {
    console.log('ğŸ” Testing generated questions in database...');
    
    // Connect to database
    connection = await mysql.createConnection({
      host: process.env.DB_HOST || 'localhost',
      port: process.env.DB_PORT || 3307,
      user: process.env.DB_USER || 'root',
      password: process.env.DB_PASSWORD || 'password',
      database: process.env.DB_NAME || 'survey_system'
    });
    
    // Check if generated_questions table exists
    const [tables] = await connection.execute("SHOW TABLES LIKE 'generated_questions'");
    if (tables.length === 0) {
      console.log('âŒ generated_questions table does not exist');
      return;
    }
    
    console.log('âœ… generated_questions table exists');
    
    // Get all generated questions
    const [rows] = await connection.execute(`
      SELECT gq.*, u.username as generated_by_name 
      FROM generated_questions gq 
      LEFT JOIN users u ON gq.generated_by = u.id 
      ORDER BY gq.created_at DESC 
      LIMIT 10
    `);
    
    if (rows.length === 0) {
      console.log('âš ï¸ No generated questions found in database');
    } else {
      console.log(`âœ… Found ${rows.length} generated questions in database:`);
      
      rows.forEach((row, index) => {
        console.log(`\nğŸ“ Question ${index + 1}:`);
        console.log(`   ID: ${row.id}`);
        console.log(`   Text: ${row.question_text}`);
        console.log(`   Type: ${row.question_type}`);
        console.log(`   Keyword: ${row.keyword}`);
        console.log(`   Category: ${row.category}`);
        console.log(`   Source Model: ${row.source_model}`);
        console.log(`   Generated By: ${row.generated_by_name || 'Unknown'} (ID: ${row.generated_by})`);
        console.log(`   Quality Score: ${row.quality_score}`);
        console.log(`   Created: ${row.created_at}`);
        if (row.options) {
          try {
            const options = JSON.parse(row.options);
            console.log(`   Options: ${options.join(', ')}`);
          } catch (e) {
            console.log(`   Options: ${row.options}`);
          }
        }
      });
    }
    
    // Get count by source model
    const [counts] = await connection.execute(`
      SELECT source_model, COUNT(*) as count 
      FROM generated_questions 
      GROUP BY source_model
    `);
    
    if (counts.length > 0) {
      console.log('\nğŸ“Š Questions by source model:');
      counts.forEach(count => {
        console.log(`   ${count.source_model}: ${count.count} questions`);
      });
    }
    
    // Get count by category
    const [categoryCounts] = await connection.execute(`
      SELECT category, COUNT(*) as count 
      FROM generated_questions 
      GROUP BY category
    `);
    
    if (categoryCounts.length > 0) {
      console.log('\nğŸ·ï¸ Questions by category:');
      categoryCounts.forEach(count => {
        console.log(`   ${count.category}: ${count.count} questions`);
      });
    }
    
  } catch (error) {
    console.error('âŒ Database test failed:', error.message);
  } finally {
    if (connection) {
      await connection.end();
    }
  }
}

async function testQuestionGeneration() {
  const BACKEND_URL = 'http://localhost:5001';
  
  try {
    console.log('\nğŸ§ª Testing question generation API...');
    
    // Login
    const loginResponse = await axios.post(`${BACKEND_URL}/api/modules/auth/login`, {
      username: 'admin',
      password: 'test123'
    });
    
    const token = loginResponse.data.data.token;
    const headers = { 'Authorization': `Bearer ${token}`, 'Content-Type': 'application/json' };
    
    // Generate questions
    const response = await axios.post(`${BACKEND_URL}/api/modules/llm/generate-questions`, {
      topic: 'Database Testing',
      count: 2,
      category: 'technology',
      userId: 1
    }, { headers });
    
    if (response.data.success) {
      const questions = response.data.data?.questions || [];
      console.log(`âœ… Generated ${questions.length} questions via API`);
      
      questions.forEach((q, index) => {
        console.log(`   ${index + 1}. ${q.question} (${q.type}) [ID: ${q.id}]`);
      });
    } else {
      console.log('âŒ Question generation failed');
    }
    
  } catch (error) {
    console.error('âŒ API test failed:', error.message);
  }
}

async function runTests() {
  console.log('ğŸš€ Testing Generated Questions Storage System\n');
  
  await testGeneratedQuestionsInDatabase();
  await testQuestionGeneration();
  
  console.log('\nğŸ‰ All tests completed!');
}

runTests();